public class pattern2 {
    public static void main(String args[]){
        int n=5;
        int j=1;
        while(j<=n){
            int i=1;
            while(i<=j){
                System.out.print("* ");
                i++;
            }
            j++;
            
            System.out.println();
            // System.out.println();
        }
    }
}
